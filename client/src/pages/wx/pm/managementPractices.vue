lang=EN-US>1</span>万元以上<span lang=EN-US>3</span>万元以下的罚款。</span><span
style='font-size:12.0pt;line-height:200%;font-family:宋体;color:black'>　　</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
