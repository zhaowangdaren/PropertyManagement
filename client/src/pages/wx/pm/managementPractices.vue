font-family:黑体;color:black'>法律责任</span></p>

<p class=MsoNormal style='margin-left:165.0pt;line-height:200%;'><span
lang=EN-US style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal style='text-indent:30.0pt;line-height:200%;'><span
style='line-height:200%;font-family:黑体;color:black'>第五十六条<span
lang=EN-US>&nbsp; </span></span><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'>有下列行为之一的，按照国务院《物业管理条例》、《安徽省物业管理条例》和《物业服务企业资质管理办法》的规定处罚：</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:24.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（一）住宅物业的建设单位未通过招投标的方式选聘物业服务企业或者未经批准，擅自采用协议方式选聘物业服务企业的，由县级以上地方人民政府房地产行政主管部门责令限期改正，给予警告，可以并处<span
lang=EN-US>10</span>万元以下的罚款。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　（二）建设单位擅自处分属于业主的物业共用部位、共用设施设备的所有权或者使用权的，由县级以上地方人民政府房地产行政主管部门处<span
lang=EN-US>5</span>万元以上<span lang=EN-US>20</span>万元以下的罚款；给业主造成损失的，依法承担赔偿责任。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　（三）不移交有关资料的，由县级以上地方人民政府房地产行政主管部门责令限期改正；逾期仍不移交有关资料的，对建设单位、物业服务企业予以通报，处<span
lang=EN-US>1</span>万元以上<span lang=EN-US>10</span>万元以下的罚款。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:24.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（四）建设单位在物业管理区域内不按照规定配置必要的物业管理用房的，由县级以上地方人民政府房地产行政主管部门责令限期改正，给予警告，没收违法所得，并处<span
lang=EN-US>10</span>万元以上<span lang=EN-US>50</span>万元以下的罚款。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:15.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（五）未取得资质证书从事物业管理的，由县级以上地方人民政府房地产行政主管部门没收违法所得，并处<span
lang=EN-US>5</span>万元以上<span lang=EN-US>20</span>万元以下的罚款；给业主造成损失的，依法承担赔偿责任。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　（六）物业服务企业聘用未取得物业管理职业资格证书的人员从事物业管理活动的，由县级以上地方人民政府房地产行政主管部门责令停止违法行为，处<span
lang=EN-US>5</span>万元以上<span lang=EN-US>20</span>万元以下的罚款；给业主造成损失的，依法承担赔偿责任。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　（七）物业服务企业将一个物业管理区域内的全部物业管理一并委托给他人的，由县级以上地方人民政府房地产行政主管部门责令限期改正，处委托合同价款<span
lang=EN-US>30%</span>以上<span lang=EN-US>50%</span>以下的罚款；情节严重的，由颁发资质证书的部门吊销资质证书。委托所得收益，用于物业管理区域内物业共用部位、共用设施设备的维修、养护，剩余部分按照业主大会的决定使用；给业主造成损失的，依法承担赔偿责任。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　（八）未经业主大会同意，物业服务企业擅自改变物业服务用房用途的，由县级以上地方人民政府房地产行政主管部门责令限期改正，给予警告，并处<span
lang=EN-US>1</span>万元以上<span lang=EN-US>10</span>万元以下的罚款；有收益的，所得收益用于物业管理区域内物业共用部位、共用设施设备的维修、养护，剩余部分按照业主大会的决定使用。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:24.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（九）业主逾期不交纳物业服务费用的，业主委员会应当督促其限期交纳；逾期仍不交纳的，物业服务企业可以向人民法院起诉。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:24.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（十） 物业服务合同解除或终止后，物业服务企业应当与业主委员会按照法律、法规规定和合同约定，办理交接手续。物业服务企业未办理交接手续擅自撤离的，由县级以上地方人民政府房地产行政主管部门责令限期改正；逾期不整改的，由县级以上地方人民政府房地产行政主管部门处五万以上十万以下的罚款。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:12.0pt;line-height:200%;font-family:
宋体;color:black'>　</span><a name=61><span style='font-size:12.0pt;line-height:
