line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>业主大会会议可以采用集体讨论的形式，也可以采用书面征求意见的形式，但应当有物业管理区域内专有部分占建筑总面积二分之一以上的业主且占总人数的二分之一以上的业主参加。凡需投票表决的，应当经业主本人签字，业主委托代理人参加业主大会会议的，应当办理书面委托手续。业主本人不办理书面委托，又不参加投票的视为弃权。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第十五条</span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'> &nbsp;</span><span style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>业主大会决定筹集和使用专项维修资金，改建、重建建筑物及附属设施的，应当经专有部分占建筑物总面积三分之二以上的业主且占总人数三分之二以上的业主同意。业主大会决定选聘物业服务企业等其他有关共有和共同管理权利的重大事项，应当经物业管理区域内专有部分占建筑物总面积过半数的业主且占总人数过半数的业主同意。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第十六条</span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'> &nbsp;</span><span style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>业主委员会除按照国务院、安徽省《物业管理条例》的相关规定履行职责外，经业主大会批准，还应当行使下列权利：　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）召集和主持业主大会会议，报告物业管理实施情况；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）经业主大会同意代表业主与选聘的物业服务企业签订物业服务合同；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）及时了解业主、物业使用人的意见和建议，监督和协助物业服务企业履行物业服务合同；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）监督管理规约的实施；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）接受业主、业主大会、物业管理行政主管部门的监督；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）建立业主委员会日常工作的规范制度和工作职责，业主委员会要正常地开展工作；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（七）根据业主大会的决定，代理因物业管理活动发生的诉讼事务；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（八）法律、法规规定以及业主大会赋予的其他职责。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第十七条</span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'> &nbsp;</span><span style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>业主委员会人数应当是<span
lang=EN-US>5--13</span>名的单数，其组成人员不得在物业服务企业中兼职。小区建筑面积为<span lang=EN-US>5</span>万平方米以下业主委员会成员为<span
lang=EN-US>5</span>名，<span lang=EN-US>5</span>万平方米以上<span lang=EN-US>15</span>万平方米以下业主委员会成员为<span
lang=EN-US>7</span>名，<span lang=EN-US>15</span>万平方米以上<span lang=EN-US>30</span>万平方米以下业主委员会成员为<span
lang=EN-US>9</span>名，<span lang=EN-US>30</span>万平方米以上业主委员会成员为不超过<span
lang=EN-US>13</span>名的单数。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>业主委员会任期一般为三年，其组成人员可以连选连任。分期开发的物业区域达到成立业主大会条件的可以先期成立。在物业区域新的一期业主入住<span
lang=EN-US>50%</span>以后，重新补选或改选业主委员会。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>业主委员会可以通过选举产生主任<span lang=EN-US>1</span>名和副主任<span
lang=EN-US>1-2</span>名，业主委员会会议由主任或者主任委托的副主任召集，召开会议必须有过半数委员出席，所作决定须经业主委员会全体委员半数以上同意。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第十八条</span><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'> </span><span lang=EN-US
style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>业主委员会任期届满<span lang=EN-US>60</span>日前，应当召开业主大会会议进行业主委员会的换届选举；逾期未换届的，物业所在地的街道办事处或乡镇人民政府应当督促或组织其换届工作。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>新一届业主委员会选举成立之日起的<span lang=EN-US>10</span>日内，原业主委员会成员应当将其保管的档案等文书资料、印章及其他属于业主大会所有的资料、用房和财物等移交新一届业主委员会，并做好其他交接工作。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第十九条</span><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'> </span><span lang=EN-US
style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>换届、选举、改选、补选业主委员会的，应自选举产生之日起的<span lang=EN-US>30</span>日内，向县、区物业管理行政主管部门、街道办事处、乡镇人民政府备案。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>业主委员会备案时应当提交以下资料：业主大会记录和会议决定；业主大会议事规则；管理规约；业主委员会成员名单和基本情况。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>县、区物业管理行政主管部门应及时将备案情况报市住建委，并书面告之居民委员会、村民委员会。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十条</span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'> &nbsp;</span><span style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>业主大会、业主委员会应当依法履行职责，不得作出与物业管理无关的决定，不得从事与本小区物业管理有关的经营活动。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>业主大会或者业主委员会的决定，对业主具有约束力。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>业主大会或者业主委员会作出的决定侵害业主合法权益的，物业所在地县、区物业管理行政主管部门或街道办事处、乡镇人民政府，应当责令改正或撤销其决定，或受侵害的业主可以请求人民法院予以撤销。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十一条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>在物业管理区域内，业主委员会、物业服务企业、居民委员会应当履行各自的职责，密切配合，共同做好物业管理区域内相关工作。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）业主委员会接受居民委员会的指导，监督与支持物业服务企业的工作；协助公安机关、居民委员会做好物业管理区域内相关工作。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）物业服务企业应在依法经营服务活动中，接受业主委员会的监督和居民委员会指导，按照合同约定为业主提供服务。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）居民委员会应当引导业主将小区物业管理与社区管理相结合，支持业主委员会和物业服务企业的各项工作。　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十二条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>业主大会和业主委员会开展工作的经费由全体业主承担。经费的筹集、管理、使用等具体事项，由业主大会议事规则规定。业主大会和业主委员会工作经费的使用情况应当定期在物业管理区域内公告，接受业主监督。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十三条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>实行物业管理联席会议制度。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业管理联席会议由县、区物业管理行政主管部门会同街道办事处或乡镇人民政府召集，居民委员会或者村民委员会、物业服务企业、专业经营单位、城市管理执法、公安派出所等单位、业主委员会或者业主代表参加。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业管理联席会议主要协调解决下列事项：</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）业主委员会未依法履行职责；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）业主委员会未依法换届；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）物业服务企业未依法退出和办理交接手续；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）物业管理与服务过程中发生的重大矛盾纠纷；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）其他需要协调解决的相关事项。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三章 </span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'>&nbsp;&nbsp;&nbsp;</span><span
style='line-height:200%;font-family:黑体;color:black'>物业服务企业</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十四条</span><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业，是指依法设立、具有独立法人资格，从事物业管理服务活动的企业。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十五条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业实行资质管理制度。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业企业资质等级分为一、二、三级。一级资质物业服务企业可以承接各种物业管理项目。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>二级资质物业服务企业可以承接<span lang=EN-US>30</span>万平方米以下的住宅项目和<span
lang=EN-US>8</span>万平方米以下的非住宅项目的物业管理业务。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>三级资质物业服务企业可以承接<span lang=EN-US>20</span>万平方米以下的住宅项目和<span
lang=EN-US>5</span>万平方米以下的非住宅项目的物业管理业务。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>未取得物业服务资质证书的企业或单位不得从事物业管理工作。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>外地物业服务企业进入本市从事物业管理活动应当到市物业管理行政主管部门办理备案手续。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十六条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务实行项目经理制。一个物业服务项目确定一名项目经理，项目经理应按照国家规定取得职业资格证书以及马鞍山市物业服务项目经理资格证书。其他从事物业管理的人员按照国家有关规定，应当取得相应的职业资格证书。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第二十七条</span><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业享有下列权利：　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）依照有关法律、法规规定和物业服务合同，对物业及其环境、相关秩序进行管理；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）依照省、市有关规定和物业服务合同收取物业服务费用和按规定应收取的其他费用；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）选聘专营企业承担专项经营业务；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）制止违反物业管理规约的行为；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）开展其他多种经营服务项目，增强企业实力；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）按照合同约定依法提前解除物业服务合同。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（七）法律、法规规定或业主大会授予的其他权利。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span style='line-height:200%;font-family:
黑体;color:black'>第二十八条</span></b><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业应当履行下列义务：　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）以为业主服务为宗旨，提高服务质量和水平；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）按照物业服务合同，提供安全防范、卫生保洁、共用部位及设施设备日常维修养护等服务；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）接受业主、业主委员会的监督；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）按照规定公布物业服务费收支情况；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）劝阻违章搭建或者其他侵害业主公共利益的行为，劝阻不听的，及时报告业主委员会和政府有关行政主管部门；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）接受政府有关行政主管部门的监督和行业协会的自律管理；　　</span></p>

<p class=MsoNormal align=left style='margin-left:29.8pt;text-align:left;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（七）协助街道、社区做好便民服务和开展社区文化活动。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（八</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>）按照合同约定，依法提前解除物业服务合同，应当在<span lang=EN-US>30</span>日前书面告知合同的另一方，在物业管理区域内公告，并书面告知市、区物业管理主管部门、街道办事处或乡镇人民政府。</span></p>

<p class=MsoNormal align=left style='margin-left:29.8pt;text-align:left;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（九）法律、法规规定的其他义务。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第四章<span lang=EN-US>&nbsp;&nbsp;&nbsp;&nbsp;</span>前期物业管理</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span style='line-height:200%;font-family:
宋体;color:black'>第二十九条</span></b><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>前期物业管理是指业主、业主大会选聘物业服务企业之前，由建设单位选聘物业服务企业的物业管理。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span lang=EN-US style='line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></b><b><span style='
line-height:200%;font-family:宋体;color:black'>第三十条</span></b><b><span
style='line-height:200%;font-family:宋体;color:black'> </span></b><span
style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>建设单位在预（销）售物业之前应当通过招投标的方式选聘具有相应资质的物业服务企业实施前期物业管理，投标人少于三个或者建筑面积<span
lang=EN-US>5</span>万平米以下的住宅物业经市物业管理主管部门批准，可以采用协议方式选聘物业服务企业。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'>5</span><span style='font-size:
15.0pt;line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>万至<span
lang=EN-US>10</span>万平方米（含<span lang=EN-US>5</span>万和<span lang=EN-US>10</span>万）的项目，可以采用邀请招标的方式，住宅物业超过规划总建筑面积<span
lang=EN-US>10</span>万平方米以上的项目应当实行公开招投标。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span style='line-height:200%;font-family:
黑体;color:black'>第三十一条<span lang=EN-US>&nbsp; </span></span></b><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>建设单位在预（销）售物业之前，应当制定临时管理规约。临时管理规约不得侵害物业买受人的合法权益。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业买受人在与建设单位签订物业买卖合同时，应当对遵守临时管理规约予以书面承诺。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>临时管理规约的效力至业主大会审议通过的管理规约生效时终止。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span style='line-height:200%;font-family:
黑体;color:black'>第三十二条</span></b><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>建设单位按照以下规定时限完成前期物业管理招投标工作：　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）预售住宅商品房项目应当在取得《商品房预售许可证》之前完成；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）新建现售住宅商品房项目应当在现售前完成；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）非出售的新建住宅物业应当在交付使用前完成。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>建设单位应当自确定中标之日起<span lang=EN-US>15</span>日内，与中标的物业服务企业签订书面的前期物业服务合同，并向物业项目所在地的县、区物业行政主管部门登记后报市住建委备案。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>签订前期物业服务合同期限一般不超过三年。业主委员会成立后与物业服务企业签订的物业服务合同生效时，前期物业服务合同自然终止。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三十三条 </span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>实行物业服务用房和物业经营性用房备案制度。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>开发建设单位在申请办理商品房预售许可证前，应当向市物业管理主管部门办理小区物业服务用房和经营性用房的备案。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三十四条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务用房和经营性用房的配置应当符合下列规定。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）物业管理区域物业总建筑面积</span><span lang=EN-US
style='line-height:200%;font-family:宋体;color:black'>5</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>万平方米（含）以下的，按照不少于建筑面积<span lang=EN-US>100</span>平方米配置物业服务用房。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）物业管理区域物业总建筑面积<span lang=EN-US>25</span>万平方米（含）以下的，按照物业总建筑面积<span
lang=EN-US>2</span>‰配置，超过部分按<span lang=EN-US>1</span>‰的标准配置物业服务用房。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）物业服务经营性用房，按照物业管理区域物业总建筑面积<span lang=EN-US>1.2</span>‰的比例在规划定位的商业用房中配置。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）农民安置房小区物业服务经营性用房，按照物业管理区域物业总建筑面积<span
lang=EN-US>8</span>‰的比例在规划定位的商业用房中配置。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）物业服务用房应当在地面以上，一般安排在住宅中心区域或者住宅区出入口附近。配置电梯的物业，物业服务用房原则上在一层安排，没有配置电梯的物业，物业服务用房楼层不得高于三层，并且具备采光、通风、水、电通信的正常使用功能，符合办理权属登记条件。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业服务用房应当包括物业服务办公用房、业主委员会办公用房等。其中，业主委员会办公用房面积不低于<span
lang=EN-US>20</span>平方米。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>建设单位移交物业服务用房、物业服务经营性用房不得收取任何费用。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><b><span style='line-height:200%;font-family:
黑体;color:black'>第三十五条</span></b><span style='line-height:200%;
font-family:宋体;color:black'> </span><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>新建住宅小区的配套建筑及设施设备符合下列条件后，建设单位方可办理物业交付手续。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）水、电纳入城市管网，安装分户计量装置和控制装置，并对物业服务用房、公用场地、公用设施设备配置独立的水、电计量装置；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）在城市管道燃气主管网覆盖的区域，完成住宅室内外燃气管道的铺设且与相应管网连接，并按照规划要求安装分户计量装置和控制装置；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）电话通信线、电视线和宽带数据传输信息端口铺设到户，安全监控装置及其他安全防范设施设备按照规划设计要求配置到位；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）按照规划要求完成消防设施建设；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）按照规划要求完成教育、文化、医疗卫生、体育、环境卫生等设施以及社区管理用房建设；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）按照规划要求完成小区道路建设，并与城市道路或公路相连；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（七）按照规划要求完成绿化建设及车库、车位的配置。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三十六条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>新建、扩建、改建工程项目的二次供水，按照以下办法办理：</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）水压要求超过国家规定的标准时，应当设置二次供水设施。建设单位应当征求供水管理部门和企业对二次供水设计方案的意见，设计方案应当满足与城市供水管网连接的基本条件和管理要求。供水工程设计方案中必须包括水表出户、一户一表、计量到户的设计内容，二次供水设施应当与主体工程同时设计、同时施工，同时交付使用。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）二次供水工程竣工后，建设单位应当组织卫生监督部门、供水企业参与验收，并经卫生监督部门认证的水质检测机构检测合格后，方可投入使用。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）经验收和水质检测合格后的二次供水设施，应当交由供水企业统一管理维护，收费到业主，直接为业主服务。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>收费标准由市物价部门核定。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三十七条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>在办理物业承接验收手续时，建设单位应当向物业服务企业移交下列资料：</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）竣工总平面图、单体建筑、结构、设备竣工图、配套设施、地下管网工程竣工图、分户验收等竣工验收资料；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）设施设备的安装，使用和维护保养等技术资料；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（三）物业质量保修文件和物业使用说明文件；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（四）物业管理区域内各类建筑物、场地、设施设备的清单；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）园林施工图纸及树种清单；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）业主名册；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（七）物业的使用、维护、管理必需的其他资料。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='line-height:200%;
font-family:宋体;color:black'>&nbsp;</span><span style='
line-height:200%;font-family:黑体;color:black'>第三十八条</span><span lang=EN-US
style='line-height:200%;font-family:宋体;color:black'>&nbsp; </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>实行新建住宅交付使用查验制度。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>新建住宅项目竣工后，开发建设单位应当组织竣工验收，办理竣工验收备案手续，相关部门和单位对公建配套设施设备建设情况进行查验，符合物业交付条件的由市住建委出具“新建住宅符合交付使用条件证明书”。对未取得“新建住宅符合交付使用条件证明书”的，开发建设单位不得向业主办理物业交付手续，房地产开发项目资本金暂缓退还。
</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第五章 </span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'>&nbsp;</span><span style='
line-height:200%;font-family:黑体;color:black'>物业服务与收费</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第三十九条</span><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'> &nbsp;&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>一个物业管理区域只能有一个物业服务企业实施物业服务。其中分期建设的物业，只能委托同一物业服务企业服务。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>物业服务企业可以将物业管理区域内的专项服务委托专业公司实施，但不得将物业的整体或者主要管理服务责任委托给他人，如因委托造成的损失由物业服务企业承担。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第四十条</span><b><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span></b><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务收费应当遵循合理、公平、公开、质价相符的原则。物业服务收费实行政府指导价和市场调节价。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>普通住宅公共服务收费实行政府指导价。政府指导价由市物价局会同市住建委，根据物业类型、服务内容、服务等级和物价指数等情况，制定相应的基准价和浮动幅度，并向社会公布。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>非普通住宅及非住宅物业的公共服务收费实行市场调节价。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第四十一条</span><b><span lang=EN-US style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>&nbsp; </span></b><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>业主应当根据物业服务合同的约定按时足额交纳物业服务费用，不应以物业服务合同约定以外的事项为理由，迟交或拒交物业服务费用。业主与使用人约定由物业使用人交纳物业服务费用的，从其约定，业主负连带责任。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>纳入物业管理范围的已竣工但尚未出售或者因开发建设单位的原因，未按时交给物业买受人的物业，物业服务费用由开发建设单位全额交纳。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>根据房屋销售合同约定，已经验收具备交付条件的物业，因业主的原因未及时办理入住手续的物业服务费用，由业主全额交纳。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>前期物业管理小区，开发建设单位取得“新建住宅符合交付使用条件证明书”后，业主从住宅符合交付使用的次月起交纳物业服务费。办理交付使用手续之前的物业服务费由开发建设单位交纳。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第四十二条</span><b><span lang=EN-US
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>&nbsp; </span></b><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'>物业服务企业应当按照国家和省有关规定公布物业服务费收支情况，业主委员会应当对所公布的物业服务费进行监督核查，并将核查报告在本物业管理区域内公布。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第四十三条<span lang=EN-US>&nbsp; </span></span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业管理区域内，供水、供电、供气、通信、有线电视等单位应当向最终用户收取有关费用，不能强制物业服务企业代收代缴。最终用户是指接受供水、供电、供气、通信、有线电视等服务的最终分户使用人。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><b><span lang=EN-US style='
line-height:200%;font-family:宋体;color:black'>&nbsp;</span></b></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>上述单位向业主或者使用人收取费用等服务事项，可以委托给物业服务企业实施，双方应当签订有偿服务合同，物业服务企业接受委托代收前款有关费用的，不得向业主收取手续费等额外费用。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><b><span style='line-height:
200%;font-family:黑体;color:black'>第四十四条<span lang=EN-US>&nbsp; </span></span></b><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务必须符合马鞍山市住宅小区物业服务等级标准。住宅小区物业服务等级标准由市住建委会同市物价局制定。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第四十五条<span lang=EN-US>&nbsp; </span></span><span
lang=EN-US style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业所签订物业服务合同应报送区、市两级物业主管部门登记备案后，方可办理物业服务收费相关手续。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第四十六条 </span><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'>市住建委每年对物业管理小区进行质量和服务等级考评，将考核结果记入该企业诚信档案，并向社会公布考核结果。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>市物业管理行业协会应当建立行业自律机制，制定并实施行业自律规范，加强对会员单位及物业服务从业人员的自律性监管，促进物业服务企业依法诚信经营。对违反行业自律规范、损害业主权益的行为应当采取相应的行业自律措施。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第六章 </span><span lang=EN-US style='line-height:
200%;font-family:宋体;color:black'>&nbsp;&nbsp;</span><span style='font-size:
15.0pt;line-height:200%;font-family:黑体;color:black'>物业的使用与维护</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span lang=EN-US style='font-size:12.0pt;line-height:200%;
font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第四十七条<span lang=EN-US>&nbsp; </span></span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业应当对业主或者物业使用人装饰装修房屋进行指导和监督。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　业主或者物业使用人装饰装修房屋，应当事先告知物业服务企业<span
lang=EN-US>;</span>物业服务企业应当将房屋装饰装修中的禁止行为和注意事项告知业主或者物业使用人，并加强巡查。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:24.0pt;
line-height:200%;'><span lang=EN-US style='font-size:12.0pt;
line-height:200%;font-family:宋体;color:black'>&nbsp;</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第四十八条<span lang=EN-US>&nbsp; </span></span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业出租的，业主应当在签订物业租赁合同后，将物业承租人、出租期限、物业服务费用交纳的约定等情况书面告知业主委员会和物业服务企业。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:9.0pt;line-height:200%;font-family:
宋体;color:black'>　<span lang=EN-US>&nbsp;&nbsp;&nbsp; </span></span><span
style='line-height:200%;font-family:黑体;color:black'>第四十九条<span
lang=EN-US>&nbsp; </span></span><span style='line-height:200%;
font-family:"仿宋_GB2312","serif";color:black'>物业管理区域内禁止下列行为：</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>一<span lang=EN-US>)</span>拆改房屋承重结构<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>二<span lang=EN-US>)</span>侵占、损坏物业的共用部位、共用设施设备<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>三<span lang=EN-US>)</span>违法搭建建筑物、构筑物<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　<span lang=EN-US>&nbsp; (</span>四<span
lang=EN-US>)</span>违反法律、法规和管理规约，以及未经有利害关系的业主同意，将住宅改变为经营性用房<span lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>五<span lang=EN-US>)</span>擅自改变房屋外貌，影响他人生活、工作或者市容环境<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>六<span lang=EN-US>)</span>违反规定摆摊设点、占道经营<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>七<span lang=EN-US>)</span>违反规定倾倒垃圾、污水和杂物<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>八<span lang=EN-US>)</span>在建筑物、构筑物上涂写、刻画或者违反规定悬挂、张贴宣传品<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>九<span lang=EN-US>)</span>违反规定停放自行车和机动车辆<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>十<span lang=EN-US>)</span>堆放易燃、易爆、剧毒或者含有放射性物质的物品，排放有毒、有害物质或者发出超过规定标准的噪音<span
lang=EN-US>;</span></span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　<span lang=EN-US>(</span>十一<span lang=EN-US>)</span>法律、法规和规章禁止的其他行为。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　违反前款规定的，其他业主、业主委员会、物业服务企业有权予以制止。制止无效的，物业服务企业、业主委员会应当及时报告有关行政主管部门。有关行政主管部门在接到报告后，应当依法予以制止或者处理。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:9.0pt;line-height:200%;font-family:
宋体;color:black'>　　<span lang=EN-US>&nbsp;&nbsp; </span></span><span
style='line-height:200%;font-family:黑体;color:black'>第五十条</span><span
lang=EN-US style='font-size:9.0pt;line-height:200%;font-family:宋体;color:black'>&nbsp;
</span><span style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>业主或者物业使用人封闭阳台以及安装空调外机、太阳能热水器、防盗网、遮阳罩等设施的，应当遵守临时管理规约或者管理规约，保持物业的整洁、美观。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:黑体;color:black'>第五十一条</span><span style='
line-height:200%;font-family:"仿宋_GB2312","serif";color:black'>　任何单位和个人不得擅自占用业主共有的道路或者其他场地用于停放车辆。确需占用物业管理区域内业主共有的道路或者其他场地停放车辆的，应当经业主大会同意并收取车位场地使用费。车位场地使用费属于全体业主共有。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>车辆在物业管理区域内行驶、停放，应当遵守物业管理区域相关管理规定。</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>任何单位和个人停放车辆不得占用消防通道。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
黑体;color:black'>第五十二条<span lang=EN-US>&nbsp; </span></span><span lang=EN-US
style='line-height:200%;font-family:宋体;color:black'>&nbsp;</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业管理区域内下列共用部位、共用设施归全体业主共有，其所有权不得分割、转让、抵押，其用途不得擅自改变：</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（一）物业管理用房；　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（二）门卫室、监控室、底层架空层、共用走廊；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（三）按规划配套建设使用的公用非机动车车库、露天机动车位；　</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:32.0pt;
line-height:200%;'><span style='font-size:16.0pt;line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（四）</span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>公共绿化、道路、场地；　　</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（五）建设单位在房屋销售合同中未明确所有权归属，且不能证明其享有所有权的其它配套设施；</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>（六）其它依法归全体业主的设施设备。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:9.0pt;line-height:200%;font-family:
宋体;color:black'>　　<span lang=EN-US>&nbsp;&nbsp; </span></span><span
style='line-height:200%;font-family:黑体;color:black'>第五十三条 </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业共用部位、共用设施设备维修更新时，相邻业主、物业使用人应当予以配合<span lang=EN-US>;</span>因相邻业主、使用人阻挠维修更新造成其他业主、使用人财产损失的，责任人应当负责修复或者赔偿。因物业维修更新造成相邻业主、使用人的自用部位、自用设备损坏或者其他财产损失的，责任人应当负责赔偿。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:9.0pt;line-height:200%;font-family:
宋体;color:black'>　　<span lang=EN-US>&nbsp;&nbsp; </span></span><span
style='line-height:200%;font-family:黑体;color:black'>第五十四条 </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>物业服务企业利用物业共用部位、共用设施设备进行经营性活动的，应当在征得相关业主和业主大会同意后按照规定办理相关手续。所得收益<span
lang=EN-US>30%</span>用于补贴物业管理公共服务费不足，<span lang=EN-US>70%</span>纳入专项维修资金，也可以在物业服务合同中约定。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='font-size:9.0pt;line-height:200%;font-family:
宋体;color:black'>　　<span lang=EN-US>&nbsp;&nbsp; </span></span><span
style='line-height:200%;font-family:黑体;color:black'>第五十五条</span><span
style='font-size:9.0pt;line-height:200%;font-family:宋体;color:black'> </span><span
style='line-height:200%;font-family:"仿宋_GB2312","serif";
color:black'>建设单位应当在物业建成后，将供水、供电、供气、供热、通信、电视等设施，按照规定移交给专业经营单位进行维护管理。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　供水、供电、供气、供热、通信、电视等单位，应当依法承担物业管理区域内相关管线和设施设备维修、养护的责任。</span></p>

<p class=MsoNormal align=left style='text-align:left;line-height:200%;
'><span style='line-height:200%;font-family:
"仿宋_GB2312","serif";color:black'>　　前款规定的单位委托物业服务企业代为维修、养护的，应当签订委托合同，并支付相应的维修、养护费用。</span></p>

<p class=MsoNormal style='margin-left:219.0pt;text-indent:-54.0pt;line-height:
200%;'><span style='line-height:200%;
font-family:黑体;color:black'>第七章</span><span lang=EN-US style='font-size:7.0pt;
