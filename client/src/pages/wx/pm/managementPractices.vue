200%;font-family:"仿宋_GB2312","serif";color:black'>（十二）物业服务企业出租、出借、转让资质证书的，由县级以上地方人民政府房地产主管部门予以警告，责令限期改正，并处<span
lang=EN-US>1</span>万元以上<span lang=EN-US>3</span>万元以下的罚款。　　</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（十三）物业服务企业不按照《物业服务企业资质管理办法》规定及时办理资质变更手续的，由县级以上地方人民政府房地产主管部门责令限期改正，可处<span
lang=EN-US>2</span>万元以下的罚款。</span></p>

