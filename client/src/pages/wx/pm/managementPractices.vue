<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（二）超越法定职权作出物业服务企业资质审批决定的；</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（三）违反法定程序作出物业服务企业资质审批决定的；</span></p>

<p class=MsoNormal align=left style='text-align:left;text-indent:30.0pt;
line-height:200%;'><span style='line-height:
200%;font-family:"仿宋_GB2312","serif";color:black'>（四）对不具备申请资格或者不符合法定条件的物业服务企业颁发资质证书的；</span></p>

<p class=MsoNormal align=left style='margin-left:29.8pt;text-align:left;
