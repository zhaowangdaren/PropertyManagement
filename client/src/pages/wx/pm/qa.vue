<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>74</span></b><b><span
style=''>、不交纳专项维修资金的怎么办？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：不缴纳专项维修资金的，房地产管理部门将不予办理其立契过户手续和权属登记手续。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>81</span></b><b><span
style=''>、物业管理区域内的公共建筑和共用设施能否改变用途？确需改变用途的，如何办理？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业管理区域内按照规划建设的公共建筑和共用设施，是满足业主正常的生活、工作需求所必须的，不得擅自改变用途。如擅自将小区内的学校改为娱乐场所、绿地改成商业设施等做法都是不被允许的。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>业主确需改变公共建筑和共用设施用途的，应当依法办理相关手续，并告知物业服务企业；物业服务企业确需改变公共建筑和共用设施用途的，应当提请业主大会讨论决定同意后，由业主依法办理相关手续。</span></p>

