style=''>、物业服务企业在公共秩序维护服务方面承担何种职责？是否应对业主人身和财产负责？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业服务企业在公共秩序维护的主要职责是公共区域的秩序维护和公共财产的看管，包括：</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>1</span><span
style=''>）相对封闭：做到小区主要出入口昼夜有专人值班，危及人身安全储有明显标志和防范措施；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>2</span><span
style=''>）维护交通秩序：包括对机动车辆和非机动车辆的行驶方向、速度、临时停车位置进行管理，保持车辆行驶通畅；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>3</span><span
style=''>）看管公共财产：包括楼内的门、窗、消防器材及小区的表井盖、雨箅子、小品、花、草、树木、果实等；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>4</span><span
style=''>）夜间对服务范围内重点部位、道路进行不少于一次的防范检查和巡逻，巡逻不少于</span><span
lang=EN-US style='font-family:"Helvetica Neue";'>2</span><span
style=''>人，做到有计划、有记录；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>5</span><span
style=''>）有发生治安案件、刑事案件、交通事故的处置预案；发生时，应立即采取措施，并及时报警和配合公安部门进行处理。物业服务企业原则上不对业主人身和户内财产负有看护责任，但物业服务企业违反物业服务合同约定，导致业主人身和财产受到损失的，物业服务企业应依法承担赔偿责任。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>54</span></b><b><span
style=''>、物业服务企业是否可以将物业管理的专项服务委托专业公司？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：可以，一个物业管理区域应由一家物业服务企业实施统一管理。物业服务企业可以将物业管理区域内的专项服务业务（比如保洁、绿化、电梯维护等）委托给保洁公司、绿化公司、电梯公司等专业性服务企业，但不得将该区域内的全部物业管理业务以并委托给他人。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>55</span></b><b><span
style=''>、物业服务企业在履行合同义务时，是否有权对违反业主（临时）公约和物业管理规章制度的行为进行制止？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：是。物业服务企业按照业主（临时）公约的规定以及物业服务合同的授权，有权要求业主配合物业服务企业的管理服务，对业主违反业主（临时）公约和物业管理规章制度的行为进行劝阻、制止或按业主大会、业主委员会的授权采取其他处理措施。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>56</span></b><b><span
style=''>、车辆停放时停车人享有什么权利？应遵守什么规定？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：车辆停放时停车人有权按照停车管理协议的约定向收停车管理服务。同时停车人应遵守停车管理制度，交纳停车管理服务费用，在指定的区域停放车辆，停放车辆不能占用、堵塞消防通道和消防设施等。</span></p>

