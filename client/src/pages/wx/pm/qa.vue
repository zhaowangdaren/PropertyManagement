<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：公房、安居房出售后，保洁、保安等使用人交费项目由购房人承担，产权人交费项目包括绿化、管理、公共设施维护、化粪池清掏费用、电梯、水泵等，公房由原售房单位承担，安居房由职工所在单位承担。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>60</span></b><b><span
style=''>、高档住宅、非住宅类物业的物业服务收费如何确定？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：高档住宅、非住宅类物业的物业服务费采取市场调节价的定价形式。在业主大会、业主委员会成立前，按房屋买卖合同（或物业管理公约、房屋使用维护管理公约）约定的价格收取费用；业主大会、业主委员会成立后，由业主大会与物业服务企业协商定价。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>61</span></b><b><span
style=''>、交了住宅专项维修资金，还用在交大中修费吗？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：交了住宅专项维修资金后，不再交纳房屋共用部位共用设施设备大中修费用。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>62</span></b><b><span
style=''>、自用部位、自用设施的维修养护费用怎么交？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：自用部位、自用设施的维修养护费用可以按年（季度、月）交给物业服务企业，也可以发生时再交纳。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>63</span></b><b><span
style=''>、出租房屋时，物业服务费用由谁来交纳？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：出租房屋时，物业服务费用的交费义务为业主，如果业主与物业使用人约定由物业使用人交纳物业服务费用的，从其约定，业主负连带交纳责任。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>64</span></b><b><span
style=''>、开发企业已竣工但未出售的物业是否应交纳物业服务费用？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：开发企业已竣工但尚未出售的物业应全额交纳物业服务费用。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>65</span></b><b><span
style=''>、公房上市后，物业服务费用如何交纳？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：公房上市后，原有产权单位不再承担交纳物业服务费用的义务。物业服务费用由房屋购买人承担。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>66</span></b><b><span
style=''>：电梯费用如何交纳？首层要交电梯费吗？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：电梯费用由楼内全体产权人按建筑面积比例分摊，首层住户也要交纳电梯费用，业主大会另有决议以及业主大会与物业服务企业另有约定的，从其约定。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>67</span></b><b><span
style=''>、物业服务企业收取停车管理费的，在停车管理中应承担何种责任？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业服务企业应负责车辆的看管和停车秩序的维护，具体包括：有机动车、非机动车管理制度和管理方案；对进入小区的机动车辆进行登记发放凭证，出门凭证放行；保证停车有序，</span><span
lang=EN-US style='font-family:"Helvetica Neue";'>24</span><span
style=''>小事设专人看管；长期存放的，应签订存车协议，明确双方的权利义务。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>68</span></b><b><span
style=''>、装修时也主是否要交纳装修押金和装修服务费？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：装修时，业主应当按照装修服务协议缴纳装修押金和装修服务费。对于装修押金，业主装修工程完工后，没有违反协议的，物业管理公司应当退还押金。物业管理公司收取装修服务费的，应承担以下义务：</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>有健全的装修管理服务制度；查验业主装修方案，与业主、施工单位签订装修管理协议，告知业主装修注意事项；装修期间，对装修现场进行巡视与检查，严格治安、消防和房屋安全管理；对进出小区的装修车辆、装修人员实行出入证管理，调节因装修引发的邻里纠纷；业主装修结束后，应进行检查。对违反装修协议的要进行处理，问题严重的包行政管理部门；及时清运装修垃圾，集中堆放时间不得超过三天。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>69</span></b><b><span
style=''>、因开发遗留问题可以拒交物业服务费吗？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：不可以。开发遗留问题是业主和建设单位之间的法律问题，而物业管理费用是业主和物业服务企业之间的法律问题，二者不能混淆。业主拒绝交纳物业服务费用是违反物业服务合同的行为，要承担违约责任。对于开发遗留问题，业主应当与建设单位通过协商或诉讼解决纠纷。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>70</span></b><b><span
style=''>、业主或业主大会是否可以聘请专业机构对物业费用进行审计？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业服务费用实行包干制的，业主大会主要对物业服务企业的服务质量进行考核，不对物业服务费用进行审计。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>物业服务费用实行酬金制的，业主大会与物业服务企业可以在合同中约定对物业服务支出或成本进行审计。审计机构由双方共同委托，审计费用由全体业主承担，审计结果对双方都有约束力。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>71</span></b><b><span
style=''>、建立专项维修资金的法律依据是什么？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：《物业管理条例》规定：</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>“</span><span
style=''>住宅物业、住宅小区内的非住宅物业或者与单幢住宅楼结构相连的非住宅物业的业主，应当按照国家有关规定交纳专项维修资金。</span><span
lang=EN-US style='font-family:"Helvetica Neue";'>”“</span><span
style=''>专项维修资金属业主所有，专项用于物业保修期满后物业共用部位，共用设施设备的维修和更新、改造，不得挪作他用。</span><span
lang=EN-US style='font-family:"Helvetica Neue";'>”</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>72</span></b><b><span
style=''>、专项维修资金的用途是什么？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：专项维修资金专项用于住宅共用部位共用设施设备的保修期满后的维修工程，即除日常运行维修养护以外的中修、大修以及更新、改造工程。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>73</span></b><b><span
style=''>、谁应交纳商品住宅专项维修资金？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：凡是</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>1999</span><span
style=''>年</span><span lang=EN-US
