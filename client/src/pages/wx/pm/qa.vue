<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业共用部位、共用设施设备是全体业主共有，业主和物业公司都不能擅自占用。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>如需占用物业共用部位、共用设施设备，应当在征得相关业主、业主大会、物业管理公司同意后，按照规定办理有关手续。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>85</span></b><b><span
style=''>、业主、物业服务企业能否利用物业共用部位、共用设施设备进行经营？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：业主、物业服务企业不得擅自利用物业共用部位、共用设施设备进行经营。如需经营的，应当在征得相关业主、业主大会、物业服务企业同意后，按照规定办理有关手续。所得收益主要用于补充纳入住宅专项维修资金，也可以按照业主大会的决定使用。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
