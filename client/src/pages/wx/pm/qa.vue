<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>83</span></b><b><span
style=''>、因维修物业或者公共利益，业主、物业公司或其他单位需临时占用、挖掘道路和场地怎么办？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：因维修物业或者公共利益，业主或其他单位确需临时占用、挖掘道路和场地的，应征得业主委员会和物业服务企业的同意；物业服务企业确需临时占用、挖掘道路和场地的，应征得业主委员会同意，或者按照事先制定的预案进行处理。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>84</span></b><b><span
