    <p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 1</span></b><b><span
style='font-family:宋体'>、什么是物业管理？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：物业管理指业主选聘物业服务企业，由业主和物业服务企业按照物业服务合同约定，对房屋及配套的设施设备和相关场地进行维修、养护、管理，维护相关区域内的环境卫生和公共秩序的活动。主要从三个角度理解：一业主选聘有合格资质等级证书的物业服务企业来实施物业管理；二</span>
<span style='font-family:宋体'>物业管理活动中两个重要的主体是业主和物业服务企业，双方签订物业服务合同，明确双方的权利义务；三</span>
<span style='font-family:宋体'>物业管理的内容是对房屋及配套的设施设备和相关场地进行维修、养护、管理，维护相关区域内的环境卫生和公共秩序。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 2</span></b><b><span
style='font-family:宋体'>、为什么要实施物业管理？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：主要有三方面原因客观上要求实施物业管理：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）房屋产权多元化。随着我国经济体制由计划经济向市场经济转变，</span><span lang=EN-US>90</span><span
style='font-family:宋体'>年代初以来我市住房制度改革逐步变化，房屋商品化，房屋产权多元化，个人拥有房屋产权的比例越来越高，原来的公房管理体制不能适应市场化和产权转移的形势。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）人们生活水平的提高，需求增强。房屋作为价值高的资产，是大多数家庭最主要财产以及财富积累的主要形式，人们对房屋财产的使用、维护、保值提出了较强的服务要求，一方面要求住得好，房屋状况保持的好，环境整洁优美，同时还要求房屋的保值增值，这些都需要高水平的专业物业管理服务。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）物业项目建设规模大，房屋及设施设备的维护管理要求专业性程度高。一个物业项目里往往有成百上千业主，众多业主由于房产毗邻，在使用管理物业、在物业的保值增值等方面，全体业主形成公共性物业管理服务需求，而且房屋建筑结构及设施设备较为复杂，对物业管理服务要求转业文化程度高。这种公共性的物业管理服务不可能由单个业主或少数业主来完成，需要通过全体业主委托专业化的物业服务企业来提供这种服务。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 3</span></b><b><span
style='font-family:宋体'>、什么是业主？业主在物业管理活动中主要有什么权利和义务？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：房屋所有权人为业主。在成立业主大会及组建业主委员会过程中，除房屋权属证书上记载的权利人可以认定为“业主”外，购买预售商品房（含经济适用房），尚未取得房屋权属证书的，房屋买卖合同记载的“购房人”可以视为“业主”。业主是物业管理活动中的重要主体，物业管理离不开业主的参与。业主的物业管理权利和义务是建立在对房屋拥有的财产权基础上，业主既拥有物业管理区域内的一份房产，也是物业管理区域的成员。业主在物业管理中享有权利，同时也要履行相应的义务。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>业主在物业管理活动中享有的权力主要有：接受物业服务；提议召开业主大会，就物业管理的有关事项提出建议；参加业主大会会议，行使投票权；选举业主委员会委员，并享有被选举权等。同时负有遵守管理规约和业主大会议事规则，执行业主大会决定和业主大会授权业主委员会作出的决定，交纳物业服务费用等义务。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 4</span></b><b><span
style='font-family:宋体'>、为什么购房时要关注物业管理事项？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：购房时就关注物业管理，才能为将来的物业管理打下好的基础。因为在业主大会成立之前的前期物业管理阶段，物业服务费用、服务内容和质量要求等有关物业管理事项按房屋买卖时的约定实行。如果购房时没有约定，房屋买卖关系已经形成，业主到入住时发现物业服务费用或者物业服务内容等不满意，就容易产生矛盾。因此在购房时要与开发单位书面约定好房屋入住后的有关物业管理事项，即使是预售房屋，购房时离入住还有一段时间，也应在购房时与开发单位约定好物业管理事项，才能有效避免入住后的物业管理纠纷。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 5</span></b><b><span
style='font-family:宋体'>、购房时应注意哪些物业管理事项？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：主要签订好两个文件：房屋买卖合同和业主临时管理规约。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>购房时与开发公司签订的房屋买卖合同中要包含有物业服务费用、物业服务内容和质量要求等内容；买房人同时要承诺遵守业主临时管理规约。</span></p>

<p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 6</span></b><b><span
style='font-family:宋体'>、是否可以到办理入住手续时再看业主临时管理规约？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：买房人在购房时就要看业主临时管理规约，在签订房屋买卖合同的同时要书面承诺遵守临时管理规约。建设单位应当在销售房屋前，制定业主临时管理规约，在房屋销售时，将业主临时管理规约向买房人明示，并予以说明；不能到办理入住手续时再将业主临时管理规约出示给买房人看。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 7</span></b><b><span
style='font-family:宋体'>、业主临时管理规约具有什么作用？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主临时管理规约要求物业管理区域内全体业主共同遵守，能起到规范业主在物业管理活动中行为，维护全体业主整体权益的作用。例如</span><span
lang=EN-US>,</span><span style='font-family:宋体'>业主临时管理规约规定不允许在小区内堆放易燃、易爆等危险物品，就是为了保证小区全体住户的安全；又如业主临时管理规约规定对欠缴费用的业主，业主委员会和物业服务企业有权公布欠费名单催缴，是为了维护全体业主的利益，对欠费业主的约束。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 8</span></b><b><span
style='font-family:宋体'>、业主临时管理规约包括哪些主要内容？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主临时管理规约由开发企业在售房前制订，主要对有关物业的使用、维护、管理，业主的共同利益，业主应当履行的义务，违反公约应承担的责任等事项依法作出约定。开发企业制订的业主临时管理规约不能侵犯购房人的合法权益。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US> 9</span></b><b><span
style='font-family:宋体'>、违反业主临时管理规约怎么处理？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主违反业主临时管理规约的，应该承担相应的违约责任，比如个别业主不按照公约的规定使用公用部位公用设施设备，造成损坏，是对其他业主的利益侵害，应该赔偿损失。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>对业主违反业主临时管理规约的行为，相关业主、业主委员会，物业服务企业、可以督促其改正；协商调解不成的，可以提起诉讼；违反相关政策法规的，报告有关部门。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>10</span></b><b><span
style='font-family:宋体'>、新的业主临时管理规约制度从什么时候开始实行？原来已经签署的物业管理公约是否继续有效？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：</span><span lang=EN-US>2003</span><span
style='font-family:宋体'>年</span><span lang=EN-US>12</span><span
style='font-family:宋体'>月</span><span lang=EN-US>9</span><span style='font-family:
宋体'>日后申领预售许可证的新建项目，实行新的业主临时管理规约制度。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>原来已经签署的物业管理公约（或房屋使用、管理、维修公约），在业主大会通过新的公约前，原公约继续有效。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>11</span></b><b><span
style='font-family:宋体'>、业主临时管理规约和管理规约有何区别？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主临时管理规约和管理规约均对物业管理区域内全体业主具有约束力。业主临时管理规约适用于业主大会成立前的前期物业管理阶段；管理规约是业主大会成立后，经全体业主所持投票权三分之二以上通过后生效。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>12</span></b><b><span
style='font-family:宋体'>、房屋买卖合同中包含哪些有关物业管理的条款？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：新《商品房买卖合同》示范文本第二十九条、二十条以及附件四均为与物业管理有关的条款。内容包括物业服务费用、物业服务内容和质量要求以及承诺遵守业主临时管理规约等。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>13</span></b><b><span
style='font-family:宋体'>、开发企业制订的房屋买卖合同中有关物业管理的内容，购房人为什么不能随意修改，是不是霸王条款？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：由于物业管理服务具有公共性的特点，绿化、保洁、公共秩序维护等物业服务，服务内容和服务水平，对入住的每个业主都是一样的，支付的费用也应该是一样的。如果与每个业主约定的公共性服务内容不一样，没有可操作性；如果约定的费用不一样，一样的服务水平，有的费用高，有的费用低，对多付费的业主不公平。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>因此，房屋买卖合同中关于公共性物业服务的费用，服务内容和质量要求等条款应由开发企业根据前期物业服务合同确定，与前期物业服务合同保持一致，开发企业在售房时要将物业服务内容和费用等明示给买房人并进行约定，但不能由个别购房人与开发单位单独协商确定。这是由于前期物业管理的特殊性决定的，目的是为了维护全体业主的合法权益，不应理解为霸王条款。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>14</span></b><b><span
style='font-family:宋体'>、房屋买卖合同中约定的物业服务费、服务内容和质量等在什么阶段实行？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：在业主大会成立前的前期物业管理阶段实行。业主大会委托业主委员会与物业服务企业签订新的物业服务合同生效时，物业服务价格、服务内容和质量等按新合同执行。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>15</span></b><b><span
style='font-family:宋体'>、前期物业服务合同如何签订，包含哪些主要内容？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：前期物业服务合同由建设单位与物业服务企业签订，包含主要内容为前期物业管理阶段的服务费用、服务内容和质量要求、双方的权利义务等。前期物业服务合同必须采取书面形式。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>16</span></b><b><span
style='font-family:宋体'>、业主大会成立前，业主能否以未与物业服务企业签订合同为由拒绝交纳物业服务费用？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：不能。因为按照规定，前期的物业服务企业由开发建设单位委托，业主应当按照约定交纳物业服务费用，接受物业服务企业的服务。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>17</span></b><b><span
style='font-family:宋体'>、业主大会委托业主委员会与物业服务企业签订物业服务合同后，单个业主能否以未与物业服务企业签订合同为由拒绝交费？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：不能。业主大会的决定对物业管理区域内的全体业主具有约束力。业主必须履行业主大会委托业主委员会与物业服务企业签订的物业服务合同的约定，交纳物业服务费用。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>18</span></b><b><span
style='font-family:宋体'>、什么是业主大会？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：物业管理区域内全体业主组成业主大会。业主大会是对应物业管理区域成立的，一个物业管理区域成立一个业主大会。业主大会代表和维护物业管理区域内全体业主在物业管理活动中的合法权益。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>19</span></b><b><span
style='font-family:宋体'>、如何划分物业管理区域？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：物业所在地区、县国土房管局会同街道办事处负责协调物业管理区域划分工作。划分住宅物业管理区域应当考虑建筑规模、自然形成、设施设备共用程度及社区建设等因素；非住宅区域划分主要考虑建设立项、规划等因素；住宅与非住宅结构相连的区域，应本着有利于物业管理的原则划定。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>20</span></b><b><span
style='font-family:宋体'>、什么时候应成立业主大会？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：新建和已建成的商品房（包括经济适用房）入住率超过</span><span
lang=EN-US>50%</span><span style='font-family:宋体'>的或入住率不足</span><span
lang=EN-US>50%</span><span style='font-family:宋体'>但首户入住已满两年的物业管理区域；公有住房出售率超过</span><span
lang=EN-US>50%</span><span style='font-family:宋体'>的物业管理区域，应当尽快组织召开首次业主大会会议，成立业主大会，选举产生业主委员会。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>21</span></b><b><span
style='font-family:宋体'>、如何召开首次业主大会会议，成立业主大会？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：应按以下步骤召开首次业主大会会议，成立业主大会：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）成立筹备组，负责业主大会筹备工作。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>住宅物业首次业主大会会议由所在地的街道办事处负责组织业主代表、建设单位（或公有住房出售单位）、社区居委会组成业主大会会议筹备组；筹备组中的业主代表由筹备组组成单位按照业主自荐、推荐的形式在充分听取业主意见基础上确定。非住宅物业筹备组由业主代表、建设单位组成。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>筹备组的主要职责如下</span></p>

<p class=MsoNormal><span style='font-family:宋体'>①确定首次业主大会会议召开的时间、地点、形式和内容；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>②按照政府主管部门制订的示范文本，拟定《业主大会议事规则》（草案）和《管理规约》（草案）；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>③确定业主身份，确定业主在首次业主大会会议上的投票权；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>④确定业主委员会委员候选人产生办法及名单；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>⑤做好召开首次业主大会会议的其他准备工作。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>上述工作①</span><span lang=EN-US>- </span><span
style='font-family:宋体'>④应以书面形式在物业管理区域内公告。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）筹备组应自组成之日起</span><span lang=EN-US>30</span><span
style='font-family:宋体'>日内组织召开首次业主大会会议，审议通过《业主大会议事规则》和《管理规约》，并选举产生业主委员会。区、县国土房管局对筹备组召开业主大会会议工作进行指导、监督。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>业主大会自首次业主大会会议召开之日起成立。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>22</span></b><b><span
style='font-family:宋体'>、什么情况下可以不成立业主大会？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：物业管理区域只有一个业主，或者业主人数较少且经全体业主一致同意，可以不成立业主大会。决定不成立业主大会的，业主共同履行业主大会、业主委员会职责。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>23</span></b><b><span
style='font-family:宋体'>、如何确定业主在首次业主大会会议的投票权？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：首次业主大会会议的投票权按照业主拥有的物业建设面积计算。以每一平米为投票权计算基数，不足一平米部分不予计算。以后业主大会会议的投票权计票方式可以通过《业主大会议事规则》确定。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>24</span></b><b><span
style='font-family:宋体'>、业主大会会议以什么形式召开？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会会议可以采取集体讨论形式，也可以采取书面征求意见形式。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>但应当有物业管理区域内持有</span><span
lang=EN-US>1/2</span><span style='font-family:宋体'>以上投票权的业主参加。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>业主因故不能参加业主大会会议，可以书面委托代理人参加。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>25</span></b><b><span
style='font-family:宋体'>、业主大会是否要定期开会，由谁召开？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会会议分为定期会议和临时会议。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>业主大会定期会议应当按照业主大会议事规则的规定由业主委员会组织召开。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>有下列情况之一的，业主委员会应当及时组织召开业主大会临时会议：</span></p>

<p class=MsoNormal><span lang=EN-US>(1)20%</span><span style='font-family:宋体'>以上业主提议的；</span></p>

<p class=MsoNormal><span lang=EN-US>(2)</span><span style='font-family:宋体'>发生重大事故或者紧急事件需要及时处理的；</span></p>

<p class=MsoNormal><span lang=EN-US>(3)</span><span style='font-family:宋体'>业主大会议事规则或管理规约规定的其他情况。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>26</span></b><b><span
style='font-family:宋体'>、业主大会具有哪些主要职责？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会主要职责如下：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）制定、修改管理规约和业主大会议事规则；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）选举、更换业主委员会委员，监督业主委员会的工作；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）选聘、解聘物业服务企业；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）决定专项维修资金使用、续筹方案，并监督实施；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>5</span><span
style='font-family:宋体'>）制定、修改物业管理区域内物业共用部位和共用设施设备的使用、公共秩序和环境卫生的维护等方面的规章制度；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>6</span><span
style='font-family:宋体'>）改建、重建建筑物及其附属设施；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>7</span><span
style='font-family:宋体'>）法律、法规或者业主大会议事规则规定的其他有关物业管理的职责。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>27</span></b><b><span
style='font-family:宋体'>、业主大会如何决议才有效？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：一般事项，必须经与会业主所持投票权</span><span
lang=EN-US> 1/2</span><span style='font-family:宋体'>以上通过；业主大会作出专项维修资金使用、续筹方案的决定，改建、重建建筑物及其附属设施，须经物业管理区域内全体业主所持投票权</span><span
lang=EN-US> 2/3</span><span style='font-family:宋体'>以上通过。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>28</span></b><b><span
style='font-family:宋体'>、什么是业主委员会，与业主大会是什么关系？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主委员会由业主大会选举产生，是业主大会的执行机构，具体实施业主大会的决议。业主委员会组成人数一般为</span><span
lang=EN-US>5</span><span style='font-family:宋体'>至</span><span lang=EN-US>9</span><span
style='font-family:宋体'>人的单数，业主委员会主任、副主任在业主委员会委员中推选产生。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>29</span></b><b><span
style='font-family:宋体'>、业主委员会职责是什么？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主委员会的主要职责为：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）召集业主大会会议，报告物业管理的实施情况；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）代表业主与业主大会选聘的物业服务企业签订物业服务合同；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）及时了解业主、物业使用人的意见和建议，监督和协助物业服务企业履行物业服务合同；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）监督管理规约的实施；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>5</span><span
style='font-family:宋体'>）业主大会赋予的其他职责。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>业主委员会应当监督违反物业服务合同约定逾期不交纳物业服务费用的业主，限期交纳物业服务费用。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>30</span></b><b><span
style='font-family:宋体'>、什么人可以当选业主委员会委员？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：具备以下资格条件的业主可以成为业主委员会委员：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）具有完全民事行为能力的业主；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）遵守国家有关法律、法规；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）遵守业主大会议事规则、管理规约、模范履行业主义务；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）热心公益事业，责任心强，公正廉洁，具有社会公信力；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>5</span><span
style='font-family:宋体'>）具有一定组织能力；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>6</span><span
style='font-family:宋体'>）具备必要的工作时间。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>在实际工作中，鼓励和支持物业管理区域内具有业主身份的各级人大代表和政协委员作为业主委员会委员参与物业管理。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>31</span></b><b><span
style='font-family:宋体'>、业主委员会是否需要备案？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主委员会应自选举产生之日起</span><span
lang=EN-US>30</span><span style='font-family:宋体'>内，到物业所在地的区、县国土房管局备案。备案时须提交《业主委员会备案单》及业主大会议事规则、管理规约、业主大会决议、法律法规规定其它材料。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>32</span></b><b><span
style='font-family:宋体'>、业主委员会如何作出决定？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：经</span><span lang=EN-US>1/3</span><span
style='font-family:宋体'>以上业主委员会委员提议或者业主委员会主任认为有必要的，应当及时召开业主委员会会议。会议应当有过半数委员出席，作出的决定须经委员人数半数以上通过。业主委员会的决定应以书面形式在物业管理区域内及时公告。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>33</span></b><b><span
style='font-family:宋体'>、什么情况下业主委员会委员资格终止？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：出现以下情况时业主委员会委员资格终止：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）因物业转让，灭失等原因不在是业主的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）无故缺席业主委员会会议连续三次以上的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）因疾病等原因丧失履行职责能力的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）有犯罪行为的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>5</span><span
style='font-family:宋体'>）以书面形式向业主大会提出辞呈的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>6</span><span
style='font-family:宋体'>）拒不履行业主义务的；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>7</span><span
style='font-family:宋体'>）其他原因不宜担任业主委员会委员的。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>34</span></b><b><span
style='font-family:宋体'>、业主大会、业主委员会是否可以作出于物业管理无关的决定？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会、业主委员会应当依法履行职责，不得作出于物业管理无关的决定，不得从事与物业管理无关的活动。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>35</span></b><b><span
style='font-family:宋体'>、什么是管理规约？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：管理规约由全体业主共同制定，经业主大会会议通过后，对全体业主具有约束力。主要内容是对有关物业的使用、维护、管理，业主的共同利益，业主应当履行的义务，违反公约应当承担的责任等事项。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>36</span></b><b><span
style='font-family:宋体'>、什么是业主大会议事规则？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会议事规则由业主大会制定和修改，由全体业主共同遵守。业主大会议事规则主要就业主大会的议事方式、表决程序、业主投票权确定方法、业主委员会的组成和委员任期等事项作出约定。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>37</span></b><b><span
style='font-family:宋体'>、业主大会、业主委员会与社区建设是怎样的关系？</span></b></p>

<p class=MsoNormal><span lang=EN-US>&nbsp;</span></p>

<p class=MsoNormal><span style='font-family:宋体'>答：居住区物业管理纳入社区建设，是我市城市管理的重要基础工作之一、目的是为了保障物业管理秩序，维护业主和居民物业管理合法权益，促进社区建设全面发展，按照有关规定，社区居委会指导、监督业主大会和业主委员会工作，并参与成立业主大会筹备组。业主大会、业主委员会应当配合公安机关、与社区居委会相互协作，共同做好维护物业管理区域的社会治安的相关工作。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>住宅小区业主大会会议，应当告知相关的社区居委会；业主大会、业主委员会作出的决定，应当听取社区居委会的建议；积极配合社区居委会依法履行自治管理职责，支持居民委员会开展工作，并接受其指导和监督。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>38</span></b><b><span
style='font-family:宋体'>、新建住宅是否必须进行招标选聘物业服务企业？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：是的。按照规定，国家提倡通过公开、公平、公正的市场竞争机制选择物业服务企业；新建住宅物业的建设单位，应通过招标的方式选聘具有相应资质的物业服务企业。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>39</span></b><b><span
style='font-family:宋体'>、业主大会决定续聘原物业服务企业，是否应进行招标？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：不需要。物业服务合同到期，业主大会决定续聘原物业服务企业提供服务的，可不再进行招标，直接授权业主委员会续签物业服务合同。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>40</span></b><b><span
style='font-family:宋体'>、业主大会决定更换物业服务企业的决议，应当有持有多少投票权的业主同意？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会更换物业服务企业的决议，必须经物业管理区域内全体业主所持投票权</span><span
lang=EN-US>2/3</span><span style='font-family:宋体'>以上通过。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>41</span></b><b><span
style='font-family:宋体'>、业主大会更换物业服务企业，是否应当告知社区居委会和街道办事处？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：是的。作为物业管理区域的重大事项，业主大会作出更换物业服务企业的决议后，应当及时告知当地街道办事处（乡镇人民政府）和社区居委会。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>42</span></b><b><span
style='font-family:宋体'>、业主委员会通过招标方式更换物业服务企业之前，应当就哪些事项形成决议？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：根据不同情况，业主大会应当在招标前决定以下事项：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）授权业主委员会或委托招标代理机构组织招标；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）确定采取公开招标或邀请招标方式；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）授权业主委员会直接确定中标人或由评标委员会推荐中标候选人名单后，由业主大会最终确定中标人；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）物业服务价格幅度或水平。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>43</span></b><b><span
style='font-family:宋体'>、业主委员会招标前，是否需要备案？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主委员会招标前，应当到项目所在区、县国土资源和房屋管理局备案。备案时，应提交以下资料：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）业主大会决议；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）招标公告或者投标邀请书；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）招标文件；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）法律、法规规定的其他资料。业主委员会委托招标代理机构进行招标的，还应提交双方签订的委托书。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>44</span></b><b><span
style='font-family:宋体'>、业主委员会组织招标过程中，如何保证广大业主的知情权？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：应从以下几个方面保障业主的知情权：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）业主大会会议通过书面征求意见的形式召开的，业主委员会应当在招标前将业主大会决议在物业管理区域内的显著位置向全体业主公示；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）评标过程中，现场答辩可采用公开的方式，业主可参加旁听；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）评标结束后，业主委员会可以将招标的过程情况以及评标委员会提交的评标报告向全体业主公示；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）中标企业确定后，业主委员会应将其基本情况和与其鉴定的物业服务合同的服务内容、范围、标准以及价格等主要内容在物业管理区域的显著位置向全体业主公示。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>45</span></b><b><span
style='font-family:宋体'>、业主委员会通过招标方式更换物业服务企业的，应当提前多少天告知原物业服务企业？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：原物业服务合同即将到期，业主大会决定重新选聘物业服务企业的，应当及时召开全体业主大会，并在原物业服务合同到期前三个月将有关决议书面告知物业服务企业。前期物业服务合同尚未到期，业主大会决定另行选聘物业服务企业的，应当在做出决议后及时书面告知物业服务企业。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>46</span></b><b><span
style='font-family:宋体'>、业主委员会招标应当何时完成？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会通过招标方式选聘物业服务企业的工作，应在原物业服务合同到期前一个月完成。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>47</span></b><b><span
style='font-family:宋体'>、业主委员会通过招标重新选聘物业服务企业后，应当如何组织新老企业的交接？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：业主大会依法重新选聘物业服务企业的，应当在解除与原物业服务企业的</span><span
lang=EN-US>(</span><span style='font-family:宋体'>前期）物业服务合同后，方可组织双方交接。交接活动由业主委员会组织，原物业服务企业应将下列材料移交给新物业服务企业：</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>1</span><span
style='font-family:宋体'>）竣工总平面图，单体建筑、结构、设备竣工图，配套设施、地下管网工程竣工图等竣工验收资料；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>2</span><span
style='font-family:宋体'>）设施设备的安装、使用和维护保养等技术资料；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>3</span><span
style='font-family:宋体'>）物业质量保修文件和物业使用说明文件；</span></p>

<p class=MsoNormal><span style='font-family:宋体'>（</span><span lang=EN-US>4</span><span
style='font-family:宋体'>）物业管理所必需的其他资料。交接时，应当制作交接证明并由双方企业和业主委员会加盖印章。交接完毕后，原物业服务企业应退出物业管理区域。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>48</span></b><b><span
style='font-family:宋体'>、如何与业主大会选聘的物业服务企业签订物业服务合同？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：按照有关规定，由业主委员会代表全体业主，与业主大会选聘的物业服务企业签订物业服务合同。物业服务合同生效后，全体业主均应履行合同约定。</span></p>

<p class=MsoNormal><span style='font-family:宋体'>物业服务合同主要内容包括物业管理事项、服务质量、服务费用、双方的权利义务、合同期限、违约责任等。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>49</span></b><b><span
style='font-family:宋体'>、物业服务一般包含哪些内容？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：物业服务的内容按照物业服务合同的约定实行。物业服务指的是对房屋及配套的设施设备和相关场地进行维修、养护、管理，维护相关区域内的环境卫生和秩序活动；一般包括综合管理、房屋及共用设施设备的维护管理、公共秩序维护、保洁服务、绿化管理、装饰装修管理服务、停车管理服务等。</span></p>

<p class=MsoNormal><b><span style='font-family:宋体'>问</span><span lang=EN-US>50</span></b><b><span
style='font-family:宋体'>、什么是公共性物业管理服务、特约性物业管理服务？</span></b></p>

<p class=MsoNormal><span style='font-family:宋体'>答：公共性物业管理服务指物业服务企业在物业管理区域内提供的共用部位共用设施设备维修、养护、环境保洁和公共秩序等多种内容的综合服务。一般提到的物业管理服务主要为公共性物业管理服务。特约性服务指受个别业主的单独委托提供的服务。比如户内自用部位和设备的维修、室内保洁、代订机票、报纸等。</span></p>

