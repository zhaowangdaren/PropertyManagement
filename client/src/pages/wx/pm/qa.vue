<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：一幢楼内有多个产权人的，如无特殊约定，楼顶是共用部位，属于全体业主共有。如需在小区楼顶上架设广告牌，应征得业主大会、物业服务企业同意后，依法办理有关手续。所得收益主要用于补充住宅专用维修资金，也可以按照业主大会的决定使用。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>87</span></b><b><span
style=''>、业主能否不按规定或约定封闭阳台、安装防护栏或搭建阳光棚？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：封闭阳台、安装防护栏或搭建阳光棚等行为看似是业主自家的事，但是会影响小区的环境和整体外观。一个环境整洁优美的小区不仅使居住其中的业主感到舒适，而且能起到提升物业品质、使其保值增值的作用。为了维护公共安全和公众利益，业主封闭阳台、安装防护栏或搭建阳光棚时，都应遵守《业主（临时）公约》和小区有关规章制度的规定。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>88</span></b><b><span
style=''>、邻居占用了公共楼道怎么办？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：公共楼道属于共用部位，邻居占用了公共楼道就是侵犯了相邻业主的权益，业主、业主委员会、物业服务企业可以要求其停止占用；造成共用部位损坏的，还可以要求其承担赔偿责任。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>89</span></b><b><span
style=''>、物业区域内的供气、供热、通讯、有线电视管线和设施设备由谁负责维护？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：物业区域内的供气、供热、通讯、有线电视管线和设施设备属供气、供热、通讯、有线电视等专业服务单位所有，由供气、供热、通讯、有线电视等专业服务单位委托物业服务企业负责维修养护。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>90</span></b><b><span
style=''>、物业管理公司需要入户维修，业主是否应该配合？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：小区内房屋结构相连，供水、供电、供气、供热等基础设施物业具有系统性等特点，致使物业局部安全隐患不仅会业主、使用人，还会影响到相邻的业主，甚至整个物业管理区域的安全和正常使用。即物业安全隐患往往会影响到公共利益和他人合法权益。因此，物业服务企业需要进行入户维修时，业主应当配合。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>91</span></b><b><span
style=''>、业主不履行维修责任，造成损失的，是否需要赔偿？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：由于业主不履行维修责任，致使房屋及附属设施已经或者可能危害毗连设施安全及公共安全的，业主大会、业主委员会可授权物业管理公司进行修缮，其费用由业主承担。造成损失的，业主应当赔偿。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>92</span></b><b><span
style=''>、小区内的共用设施损坏，维修费用该由谁承担？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：如果是人为造成小区公用设施损坏的，由损坏者负责修复；造成损失的，应当赔偿损失。如果责任不明确的，维修费用应该由全体业主按所拥有的建筑面积比例分担。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>93</span></b><b><span
style=''>、为什么要规范房屋装修装修行为？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：我国住宅房屋多数属于集合式楼房。在住宅小区中，住宅单体间存在共用部位，如主体结构承重部位；单幢楼或小区内有共用设施设备，如电梯、水、暖、照明、消防等设施设备。不当的装饰装修会导致结构安全和装修人自身的生命财产安全，还会影响到整幢楼、整个小区的正常生活秩序。为了维护公共安全和公众利益，需要对房屋装饰装修行为进行规范，因此，业主需要装饰装修房屋的，应当事先告知物业服务企业，物业服务企业也应将房屋装饰装修中的禁止行为和注意事项告知业主。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>94</span></b><b><span
style=''>、业主装饰装修房屋，需要事先告知物业服务企业吗？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：装饰装修房屋是业主的权利，但这一权力的行使应以不损害他人利益和社会公共利益为前提。在一个存在多业主的物业管理区域内，业主装饰装修房屋的行为有可能会对其他业主造成影响。如装修噪音影响相邻业主生活和休息、破坏房屋承重结构会对其他业主的人身和财产安全构成威胁等。同时，物业管理公司有义务根据物业服务合同的约定对物业进行管理，而对物业及其共用部位、共用设施设备的结构、功能、使用等情况的了解是完成这一义务的前提。因此，业主装饰装修房屋，应当事先告知物业管理公司。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>95</span></b><b><span
style=''>、业主装饰装修房屋，需要与物业服务企业签订装饰装修管理服务协议吗？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：业主应当在装饰装修房屋前与物业服务企业签订住宅内装饰装修管理服务协议。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>住宅室内装饰装修管理服务协议应当包括以下内容：装饰装修工程的实施内容，装饰装修工程的实施期限，允许施工的时间，废弃物的清运与处置，住宅外立面设施及防盗窗的安装要求，禁止行为和注意事项，管理服务费用，违约责任等。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>96</span></b><b><span
style=''>、住宅室内装饰装修禁止行为有哪些？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：按照建设部第</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>110</span><span
style=''>令《住宅室内装饰装修管理办法》的规定，住宅室内装饰装修活动，禁止下列行为：</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>1</span><span
style=''>）未经原设计单位或者具有相应资质等级设计单位提出设计方案，变动建筑主体和承重结构；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>2</span><span
style=''>）将没有防水要求的房间或者阳台改为卫生间、厨房；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>3</span><span
style=''>）扩大承重墙上原有的门窗尺寸，拆除连接阳台的砖、混凝土墙体；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>4</span><span
style=''>）损坏房屋原有节能设施，降低节能效果；</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>5</span><span
style=''>）未经批准，不得搭建建筑物、构筑物；改变住宅外立面，在非承重墙上开门、窗；拆改供暖管道和设施；拆改燃气管道和设施。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>（</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>6</span><span
style=''>）其他影响建筑结构和使用安全的行为。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>97</span></b><b><span
style=''>、业主装饰装修房屋过程中，物业服务企业能否进行入户检查？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：业主装饰装修房屋过程中，直至装修结束后，物业服务企业应当按照装饰装修管理服务协议进行现场检查，对违反法律、法规和装饰装修管理服务协议的予以制止；情节严重的，及时向有关部门报告。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>98</span></b><b><span
style=''>、住宅室内装饰装修的施工时间如何规定？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：按照市建委、市规委、市国土房管局《关于加强新建商品住宅家庭居室装饰装修管理若干规定</span><span
lang=EN-US style='font-family:"Helvetica Neue";'>(</span><span
style=''>试行）》，每日</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>12</span><span
style=''>时至</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>14</span><span
style=''>时，</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>18</span><span
style=''>时至次日</span><span lang=EN-US
style='font-family:"Helvetica Neue";'>8</span><span
style=''>时，不得从事敲、凿、刨、钻等产生噪声的装饰装修活动。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>99</span></b><b><span
style=''>、应如何处理住宅室内装饰装修垃圾？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：住宅室内装饰装修所形成的各种废弃物，应当按照物业服务企业指定的位置、方式和时间进行堆放和清运。严禁从楼上向地面或者垃圾道、下水道抛弃废弃物。</span></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><b><span
style=''>问</span></b><b><span lang=EN-US
style='font-family:"Helvetica Neue";'>&nbsp;100</span></b><b><span
style=''>、住宅室内装饰装修工程的保修期有多长时间？</span></b></p>

<p style='margin:0cm;margin-bottom:.0001pt;'><span
style=''>答：在正常使用条件下，住宅室内装饰装修工程的最低保修期为二年，有防水要求的厨房、卫生间和外墙面的防渗漏为五年。保修期自住宅室内装饰装修工程竣工验收合格之日起计算。</span></p>

